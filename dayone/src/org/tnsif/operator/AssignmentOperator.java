package org.tnsif.operator;

public class AssignmentOperator {
	public static void main(String []args) {
		int x=3;
		int a=2;
		int b=8;
		int c=9;
		int d=3;
		a*=x;
		b+=x;
		c/=x;
		d-=x;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(x);

	}

}
