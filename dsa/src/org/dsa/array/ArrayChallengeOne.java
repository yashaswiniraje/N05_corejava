package org.dsa.array;
import java.util.*;
public class ArrayChallengeOne {

	public static void main(String[]args) {
		Scanner sc=new Scanner(System.in);
		int[] arr= new int [3];
		for(int i=0;i<arr.length;i++) {
			arr[i]=sc.nextInt();		
	}
	for(int i=0;i<arr.length;i++) {
		System.out.println(arr[i]);		

}
}
}
